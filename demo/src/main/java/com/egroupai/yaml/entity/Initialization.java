package com.egroupai.yaml.entity;

public class Initialization {
	private String enginePath;
	private String modelPath;
	private String outputFace;
	private String outputFrame;
	
	public String getEnginePath() {
		return enginePath;
	}
	public void setEnginePath(String enginePath) {
		this.enginePath = enginePath;
	}
	public String getModelPath() {
		return modelPath;
	}
	public void setModelPath(String modelPath) {
		this.modelPath = modelPath;
	}
	public String getOutputFace() {
		return outputFace;
	}
	public void setOutputFace(String outputFace) {
		this.outputFace = outputFace;
	}
	public String getOutputFrame() {
		return outputFrame;
	}
	public void setOutputFrame(String outputFrame) {
		this.outputFrame = outputFrame;
	}
}
